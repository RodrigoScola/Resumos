function brainFried () {
    const user = new User('snuffy');
    

    user.current.brainActivity = 20;
    if (user.current.brainActivity == 100) {
        Dom.dominate();    
    }

function Dom () {
    dominate = function() {
           user.status = 'mindless';
           user.current.admin = dom;
       
        }
    }
}

function User (name) {
    name = name;
    status = 'operational';
    admin = name;
    current  = function() {
        brainActivity = 100;
        admin = name;
    }
}